const { contextBridge, ipcRenderer } = require('electron');

contextBridge.exposeInMainWorld('electronAPI', {
  getAppInfo: () => ipcRenderer.invoke('get-app-info'),
  testMessage: (message) => ipcRenderer.invoke('test-message', message),
  getWindowInfo: () => ipcRenderer.invoke('get-window-info'),
  focusWindow: () => ipcRenderer.invoke('focus-window'),
  minimizeWindow: () => ipcRenderer.invoke('minimize-window'),
  maximizeWindow: () => ipcRenderer.invoke('maximize-window'),
  restoreWindow: () => ipcRenderer.invoke('restore-window'),
  navigate: (url) => ipcRenderer.invoke('navigate', url),
  getPageInfo: () => ipcRenderer.invoke('get-page-info'),
  executeScript: (script) => ipcRenderer.invoke('execute-script', script),
  takeScreenshot: (path) => ipcRenderer.invoke('take-screenshot', path),
  getViewportSize: () => ipcRenderer.invoke('get-viewport-size'),
  setViewportSize: (width, height) => ipcRenderer.invoke('set-viewport-size', width, height),
  getAccessibilityTree: () => ipcRenderer.invoke('get-accessibility-tree'),
  waitForSelector: (selector, timeout, state) => ipcRenderer.invoke('wait-for-selector', selector, timeout, state),
  getText: (selector, timeout) => ipcRenderer.invoke('get-text', selector, timeout),
  fill: (selector, value, timeout) => ipcRenderer.invoke('fill', selector, value, timeout),
  click: (selector, timeout) => ipcRenderer.invoke('click', selector, timeout),
  select: (selector, value, timeout) => ipcRenderer.invoke('select', selector, value, timeout),
  getShadowDomInfo: () => ipcRenderer.invoke('get-shadow-dom-info'),
  getIframeContent: () => ipcRenderer.invoke('get-iframe-content'),
  getModalStatus: () => ipcRenderer.invoke('get-modal-status'),
  getTimingStatus: () => ipcRenderer.invoke('get-timing-status'),
  scrollIntoView: (selector) => ipcRenderer.invoke('scroll-into-view', selector),
  getDragStatus: () => ipcRenderer.invoke('get-drag-status'),
  getCanvasInfo: () => ipcRenderer.invoke('get-canvas-info'),
  getSvgInfo: () => ipcRenderer.invoke('get-svg-info'),
  getFormValidation: () => ipcRenderer.invoke('get-form-validation'),
  getScrollStatus: () => ipcRenderer.invoke('get-scroll-status'),
  getDisabledStatus: () => ipcRenderer.invoke('get-disabled-status'),
  getOpacityStatus: () => ipcRenderer.invoke('get-opacity-status'),
  getCustomCheckboxStatus: () => ipcRenderer.invoke('get-custom-checkbox-status'),
  getFileUploadStatus: () => ipcRenderer.invoke('get-file-upload-status'),
  evaluateInShadow: (selector, script) => ipcRenderer.invoke('evaluate-in-shadow', selector, script),
  evaluateInIframe: (selector, script) => ipcRenderer.invoke('evaluate-in-iframe', selector, script),
  clickSvgElement: (elementId) => ipcRenderer.invoke('click-svg-element', elementId),
  getConsoleMessages: () => ipcRenderer.invoke('get-console-messages'),
  getPerformanceMetrics: () => ipcRenderer.invoke('get-performance-metrics'),
  clearBrowserCache: () => ipcRenderer.invoke('clear-browser-cache'),
  getUserAgent: () => ipcRenderer.invoke('get-user-agent'),
  getProtocolInfo: () => ipcRenderer.invoke('get-protocol-info'),
  emulateNetworkConditions: (conditions) => ipcRenderer.invoke('emulate-network-conditions', conditions),
  resetNetworkConditions: () => ipcRenderer.invoke('reset-network-conditions'),
  setGeolocation: (location) => ipcRenderer.invoke('set-geolocation', location),
  clearGeolocation: () => ipcRenderer.invoke('clear-geolocation'),
  setDeviceMetrics: (metrics) => ipcRenderer.invoke('set-device-metrics', metrics)
});
